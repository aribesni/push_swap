#include "libft.h"

void	ft_putnbr(int nb)
{
	int			i;
	long int	nbr;

	i = 0;
	nbr = nb;
	if (nbr < 0)
	{
		ft_putchar('-');
		nbr = -nbr;
	}
	if (nbr > 9)
	{
		ft_putnbr(nbr / 10);
		ft_putchar(nbr % 10 + 48);
	}
	if (nbr <= 9)
		ft_putchar(nbr + 48);
}
